    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String[] raw = txtRaw.getText().trim().split("\n");
        int quatity = 0;
        try {
            quatity = Integer.parseInt(txtQuatity.getText());
            String rawEnglishs[] = HandleRaw.getValuesEvenMumber(raw).split("\n");
            String result = "";
            for (int i = 0; i < rawEnglishs.length; i++) {
                result += HandleRaw.getWord(rawEnglishs[i],quatity);
            }
            txtResult.setText(result);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Input must number");
        }

        //JOptionPane.showMessageDialog(rootPane, ""+result);
    }//GEN-LAST:event_jButton2ActionPerformed

