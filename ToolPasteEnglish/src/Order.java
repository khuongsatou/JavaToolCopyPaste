    private void txtQuatityKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtQuatityKeyTyped
        // TODO add your handling code here:
        char vChar = evt.getKeyChar();
        if (!(Character.isDigit(vChar)
                || (vChar == KeyEvent.VK_BACK_SPACE)
                || (vChar == KeyEvent.VK_DELETE))) {
            evt.consume();
        }
    }//GEN-LAST:event_txtQuatityKeyTyped

